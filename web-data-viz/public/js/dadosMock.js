function gerarListaComSeed(min, max, seed) {
  const lista = [];
  let valorSeed = seed;

  function randomSeeded() {
    valorSeed = (valorSeed * 9301 + 49297) % 233280;
    return valorSeed / 233280; 
  }

  for (let i = 0; i < 50; i++) {
    const valor = randomSeeded() * (max - min) + min;
    lista.push(Number(valor.toFixed(2)));
  }

  return lista;
}

